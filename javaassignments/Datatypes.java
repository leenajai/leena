public class Datatypes {
    public static void main(String args[]) {
      int a=10;
      float b=25.67867f;
      double c=893;
      char d='T';
      String e="Java" ;

      System.out.println("int = "+a);
      System.out.println("Float = "+b);
      System.out.println("Double = "+c);
      System.out.println("Character = "+d);
      System.out.println("String = "+e);
    }
}